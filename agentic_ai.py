def extract_amount(text):
    import re

    # Look for ₹ or Rs patterns
    matches = re.findall(r'(?:₹|rs\.?|inr)?\s?([\d,]+)', text.lower())

    amounts = []
    for m in matches:
        try:
            val = int(m.replace(',', ''))
            if val > 50:   # ignore small numbers like time
                amounts.append(val)
        except:
            pass

    if amounts:
        return max(amounts)   # take biggest → usually correct
    else:
        return 0

