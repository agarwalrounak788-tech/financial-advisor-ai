def clean_text(text):
    lines = text.split('\n')

    useful_lines = []
    keywords = ["paid", "amount", "₹", "rs", "zomato", "swiggy", "market", "fuel", "rent"]

    for line in lines:
        line_lower = line.lower()
        if any(k in line_lower for k in keywords):
            useful_lines.append(line)

    return " ".join(useful_lines)
